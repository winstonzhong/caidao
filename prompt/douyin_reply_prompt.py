def random_select_two_dimensions():
    """
    从预设的维度列表中随机选择两个不重复的元素

    Returns:
        tuple: 包含两个随机选中的维度名称的元组
    """
    # 定义所有的数据维度列表
    dimensions = [
        "投稿数",
        "投稿数同比",
        "播放量",
        "播放量同比",
        "点赞量",
        "点赞量同比",
        "完播率",
        "完播率同比",
        "互动指数",
        "作品搜索量",
        "作品搜索量同比",
        "粉丝净增",
        "播放量超越比例"
    ]

    # 随机选择2个不重复的元素
    # sample函数会确保选中的元素不重复，且数量准确
    selected = random.sample(dimensions, 2)

    # 返回元组形式（比列表更适合固定数量的返回值）
    return '+'.join(selected)



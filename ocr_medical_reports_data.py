from enum import Enum, unique

from ocr_data_final import ocr_data_gen
@unique
class 医疗报告类型(Enum):
    """医疗报告类型枚举类，用于OCR识别后报告的分类标识"""

    # 常规体检相关
    常规体检报告 = "常规体检报告"  # 综合性基础体检（含身高、体重、血尿常规等基础项）
    入职体检报告 = "入职体检报告"  # 企业要求的入职专项体检（通常含传染病筛查等）
    体检复查报告 = "体检复查报告"  # 针对首次体检异常项的复查报告
    体检总结报告 = "体检总结报告"  # 多项目体检后的综合结论（含健康建议）
    健康体检套餐报告 = "健康体检套餐报告"  # 特定套餐类体检（如中老年套餐、女性套餐等）

    # 门诊相关
    门诊报告 = "门诊报告"  # 门诊就诊后的检查/诊断结论
    门诊病历 = "门诊病历"  # 门诊接诊时的病程记录（含主诉、查体、初步诊断）
    门诊处方报告 = "门诊处方报告"  # 门诊开具的药品处方单
    急诊报告 = "急诊报告"  # 急诊科就诊产生的检查/诊断报告
    急诊病历 = "急诊病历"  # 急诊科的病程记录（含抢救、处理过程）

    # 住院相关
    住院报告 = "住院报告"  # 住院期间的综合诊疗记录
    出院小结 = "出院小结"  # 出院时的总结（含入院情况、治疗过程、出院医嘱）
    住院病历 = "住院病历"  # 详细的住院病程记录（每日病情变化）
    手术记录报告 = "手术记录报告"  # 手术过程的详细记录（含术式、术中情况）
    入院记录 = "入院记录"  # 患者入院时的基本情况、病史、初步诊断

    # 检验类报告（血液/体液等）
    血常规报告 = "血常规报告"  # 血液细胞计数等基础检查
    尿常规报告 = "尿常规报告"  # 尿液成分检测
    生化检查报告 = "生化检查报告"  # 肝肾功能、电解质等生化指标
    血糖检查报告 = "血糖检查报告"  # 空腹/餐后血糖检测
    血脂检查报告 = "血脂检查报告"  # 总胆固醇、甘油三酯等
    肿瘤标志物报告 = "肿瘤标志物报告"  # 癌胚抗原、甲胎蛋白等
    传染病筛查报告 = "传染病筛查报告"  # 乙肝、丙肝、梅毒、HIV等
    凝血功能报告 = "凝血功能报告"  # 凝血四项、凝血因子等检测
    甲状腺功能报告 = "甲状腺功能报告"  # 甲状腺激素（T3、T4、TSH等）

    # 影像/功能类报告
    超声检查报告 = "超声检查报告"  # B超、彩超（如腹部、心脏、血管超声）
    CT检查报告 = "CT检查报告"  # 计算机断层扫描（如胸部CT、头部CT）
    核磁共振报告 = "核磁共振报告"  # MRI检查（如脑部、关节MRI）
    X光检查报告 = "X光检查报告"  # 胸部、骨骼等X线检测
    心电图报告 = "心电图报告"  # 常规心电图、动态心电图（Holter）
    内镜检查报告 = "内镜检查报告"  # 胃镜、肠镜、支气管镜等
    PET_CT报告 = "PET-CT报告"  # 正电子发射断层显像（肿瘤筛查等）

    病理报告 = "病理报告"  # 组织/细胞活检后的病理诊断（如活检、手术标本）

    # 其他专项报告
    会诊报告 = "会诊报告"  # 多科室联合会诊后的结论
    远程会诊报告 = "远程会诊报告"  # 跨机构远程会诊记录
    慢病管理报告 = "慢病管理报告"  # 高血压、糖尿病等慢性病随访记录
    疫苗接种报告 = "疫苗接种报告"  # 疫苗接种记录及证明
    体检异常通知书 = "体检异常通知书"  # 针对体检异常项的单独通知（如结节、指标超标）

    # 兜底项（未覆盖的特殊类型）
    其他报告 = "其他报告"

# 使用示例
if __name__ == "__main__":
    print("医疗报告OCR文本样本数据已生成")
    print(f"共包含 {len(ocr_data_gen)} 种报告类型")
    print(f'总共定义了 {len(医疗报告类型)} 种报告类型')
    for report_type, samples in ocr_data_gen.items():
        print(f"{report_type}: {len(samples)} 个样本")
